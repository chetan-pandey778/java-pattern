public class relational_operator {
    public static void main(String aregs[]){
        // ==,<,>,<=,>=,!= these are relational operator
        int a=10;
        int b=10;
        int c=20;
        // they all gave ans in bollean i.e. true or false
        System.out.println(a==b);
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a>c);
        System.out.println(a<=b);
        System.out.println(a<=c);
        System.out.println(a>=b);
        System.out.println(a!=b);

    }
    
}
